void encoder_update_user(uint8_t index, bool clockwise) {
	switch (get_highest_layer(layer_state)) {
	    case _LOWER:
		if (clockwise) {
		    tap_code(KC_WH_D);
		} else {
		    tap_code(KC_WH_U);
		}
		break;
	    default:
		if (clockwise) {
		    tap_code(KC_PGDN);
		} else {
		    tap_code(KC_PGUP);
		}
	}
}
